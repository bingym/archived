#!/usr/bin/env node
/**
 * Parse data/gzallen/twitter.csv (时间,设备,内容) and emit SQL INSERTs for tweets.person_id = 'gzallen'.
 *
 * Usage:
 *   node scripts/import-gzallen-twitter.mjs
 *   node scripts/import-gzallen-twitter.mjs --out ./gzallen_tweets.sql
 *   node scripts/import-gzallen-twitter.mjs --no-delete   # omit DELETE, append-only
 *
 * Apply:
 *   wrangler d1 execute archived --local --file=scripts/generated/gzallen_tweets.sql
 *   wrangler d1 execute archived --remote --file=scripts/generated/gzallen_tweets.sql
 */

import { readFileSync, mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { parse } from "csv-parse/sync";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const DEFAULT_CSV = join(ROOT, "data", "gzallen", "twitter.csv");
const DEFAULT_OUT = join(ROOT, "scripts", "generated", "gzallen_tweets.sql");

const PERSON_ID = "gzallen";

function parseArgs(argv) {
  let out = DEFAULT_OUT;
  let csvPath = DEFAULT_CSV;
  let withDelete = true;
  for (let i = 2; i < argv.length; i++) {
    if (argv[i] === "--out" && argv[i + 1]) {
      out = argv[++i];
    } else if (argv[i] === "--csv" && argv[i + 1]) {
      csvPath = argv[++i];
    } else if (argv[i] === "--no-delete") {
      withDelete = false;
    }
  }
  return { out, csvPath, withDelete };
}

function sqlLiteral(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function main() {
  const { out, csvPath, withDelete } = parseArgs(process.argv);
  const raw = readFileSync(csvPath, "utf8");
  const rows = parse(raw, {
    columns: false,
    skip_empty_lines: true,
    relax_quotes: true,
    relax_column_count: true,
  });

  const dataRows = rows.slice(1);
  const now = Date.now();
  const lines = [];
  let insertCount = 0;

  lines.push(`-- Generated by scripts/import-gzallen-twitter.mjs`);
  lines.push(`-- Source: ${csvPath.replace(ROOT, ".")}`);
  lines.push("");

  if (withDelete) {
    lines.push(`DELETE FROM tweets WHERE person_id = ${sqlLiteral(PERSON_ID)};`);
    lines.push("");
  }

  for (const row of dataRows) {
    if (!Array.isArray(row) || row.length < 3) continue;
    const datetime = row[0]?.trim() ?? "";
    const device = row[1]?.trim() ?? "";
    const content = row[2] ?? "";
    /** CSV「设备」列原样写入 metadata（纯文本，非 JSON） */
    const metadata = device === "" ? null : device;
    const sql = [
      "INSERT INTO tweets (person_id, datetime, content, metadata, imgs, created_at, updated_at) VALUES (",
      [
        sqlLiteral(PERSON_ID),
        sqlLiteral(datetime || null),
        sqlLiteral(content),
        metadata === null ? "NULL" : sqlLiteral(metadata),
        "NULL",
        String(now),
        String(now),
      ].join(", "),
      ");",
    ].join("");
    lines.push(sql);
    insertCount += 1;
  }

  lines.splice(2, 0, `-- CSV rows: ${dataRows.length}, INSERTs: ${insertCount}`);

  mkdirSync(dirname(out), { recursive: true });
  writeFileSync(out, lines.join("\n") + "\n", "utf8");
  console.log(`Wrote ${out} (${insertCount} INSERTs)`);
  console.log("Apply with:");
  console.log(`  wrangler d1 execute archived --local  --file=${out}`);
  console.log(`  wrangler d1 execute archived --remote --file=${out}`);
}

main();
