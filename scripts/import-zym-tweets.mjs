#!/usr/bin/env node
/**
 * Parse zym 微博 JSON（数组项含 author / datetime / meta / text），生成 tweets 的 INSERT SQL，person_id 固定为 zym。
 * 字段对应：datetime → datetime，text → content，meta → metadata（纯文本，trim），imgs → NULL。
 *
 * 默认读仓库内 data/zym/2886.json（可从 demo 目录拷入）。也可用 --json 指定任意路径。
 *
 * Usage:
 *   node scripts/import-zym-tweets.mjs
 *   node scripts/import-zym-tweets.mjs --json /path/to/2886.json --out ./out.sql
 *   node scripts/import-zym-tweets.mjs --person zym --no-delete
 *
 * Apply:
 *   wrangler d1 execute archived --local  --file=scripts/generated/zym_tweets.sql
 *   wrangler d1 execute archived --remote --file=scripts/generated/zym_tweets.sql
 */

import { readFileSync, mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const DEFAULT_JSON = join(ROOT, "data", "zym", "2886.json");
const DEFAULT_OUT = join(ROOT, "scripts", "generated", "zym_tweets.sql");
const DEFAULT_PERSON = "zym";

function parseArgs(argv) {
  let out = DEFAULT_OUT;
  let jsonPath = DEFAULT_JSON;
  let personId = DEFAULT_PERSON;
  let withDelete = true;
  for (let i = 2; i < argv.length; i++) {
    if (argv[i] === "--out" && argv[i + 1]) {
      out = argv[++i];
    } else if (argv[i] === "--json" && argv[i + 1]) {
      jsonPath = argv[++i];
    } else if (argv[i] === "--person" && argv[i + 1]) {
      personId = argv[++i];
    } else if (argv[i] === "--no-delete") {
      withDelete = false;
    }
  }
  return { out, jsonPath, personId, withDelete };
}

function sqlLiteral(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function main() {
  const { out, jsonPath, personId, withDelete } = parseArgs(process.argv);
  const raw = readFileSync(jsonPath, "utf8");
  const data = JSON.parse(raw);
  if (!Array.isArray(data)) {
    throw new Error("JSON root must be an array");
  }

  const now = Date.now();
  const lines = [];
  let insertCount = 0;

  lines.push(`-- Generated by scripts/import-zym-tweets.mjs`);
  lines.push(`-- Source: ${jsonPath.replace(ROOT, ".")}`);
  lines.push("");

  if (withDelete) {
    lines.push(`DELETE FROM tweets WHERE person_id = ${sqlLiteral(personId)};`);
    lines.push("");
  }

  for (const item of data) {
    if (!item || typeof item !== "object") continue;
    const datetime = typeof item.datetime === "string" ? item.datetime.trim() : "";
    const content = typeof item.text === "string" ? item.text : "";
    const metaRaw = typeof item.meta === "string" ? item.meta.trim() : "";
    const metadata = metaRaw === "" ? null : metaRaw;

    if (!datetime && !content) continue;

    const sql = [
      "INSERT INTO tweets (person_id, datetime, content, metadata, imgs, created_at, updated_at) VALUES (",
      [
        sqlLiteral(personId),
        sqlLiteral(datetime || null),
        sqlLiteral(content),
        metadata === null ? "NULL" : sqlLiteral(metadata),
        "NULL",
        String(now),
        String(now),
      ].join(", "),
      ");",
    ].join("");
    lines.push(sql);
    insertCount += 1;
  }

  lines.splice(2, 0, `-- JSON items: ${data.length}, INSERTs: ${insertCount}, person_id: ${personId}`);

  mkdirSync(dirname(out), { recursive: true });
  writeFileSync(out, lines.join("\n") + "\n", "utf8");
  console.log(`Wrote ${out} (${insertCount} INSERTs)`);
  console.log("Apply with:");
  console.log(`  wrangler d1 execute archived --local  --file=${out}`);
  console.log(`  wrangler d1 execute archived --remote --file=${out}`);
}

main();
